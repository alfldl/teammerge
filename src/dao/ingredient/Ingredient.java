package dao.ingredient;

public class Ingredient {
	int i_no;
	String i_catego;
	String i_item;
	String i_img;
	
	public int getI_no() {
		return i_no;
	}
	public void setI_no(int i_no) {
		this.i_no = i_no;
	}
	public String getI_catego() {
		return i_catego;
	}
	public void setI_catego(String i_catego) {
		this.i_catego = i_catego;
	}
	public String getI_item() {
		return i_item;
	}
	public void setI_item(String i_item) {
		this.i_item = i_item;
	}
	public String getI_img() {
		return i_img;
	}
	public void setI_img(String i_img) {
		this.i_img = i_img;
	}
}

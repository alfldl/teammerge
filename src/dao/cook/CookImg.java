package dao.cook;

public class CookImg {
	private int c_no;
	private String c_po;
	private String c_po_img;
	private int po_step;
	
	public int getC_no() {
		return c_no;
	}
	public void setC_no(int c_no) {
		this.c_no = c_no;
	}
	public String getC_po() {
		return c_po;
	}
	public void setC_po(String c_po) {
		this.c_po = c_po;
	}
	public String getC_po_img() {
		return c_po_img;
	}
	public void setC_po_img(String c_po_img) {
		this.c_po_img = c_po_img;
	}
	public int getPo_step() {
		return po_step;
	}
	public void setPo_step(int po_step) {
		this.po_step = po_step;
	}
	
}
